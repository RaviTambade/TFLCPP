#include "calculator.h"

int addition(int op1, int op2){
    int result=op1+op2;
    return result;
}

int subtraction(int op1, int op2)
{
    int result=op1-op2;
    return result;
}

int multiplication(int op1, int op2){
    int result=op1*op2;
    return result;
}

int division(int op1, int op2)
{
    int result=op1/op2;
    return result;
}
